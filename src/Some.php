namespace Wow;


class Some extends Maybe
{
    private $val;
    public function __construct($val) {
        $this->val = $val;
    }

    public function __get($name) {
        if ($name == 'val') {
            return $this->val;
        }

        assert(false, 'Some just get value by val ');
    }

    public function __toString() {
        return sprintf('Some %s', $this->val);
    }
}